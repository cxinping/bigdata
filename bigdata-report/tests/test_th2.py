import threading
import time

from werkzeug.local import Local

my_1 = Local()
my_1.b = 0

# 新线程中，b的值被修改
def worker():
    my_1.b = 1
    print(my_1.b)


new_t = threading.Thread(target=worker)
new_t.start()
time.sleep(1)
# 主线程中，b的值没有改变
print(my_1.b)

